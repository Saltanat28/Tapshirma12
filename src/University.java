public class University {
    String name;
    String location;
    int rating;
    void setName( String name){
        this.name = name;

    }
    String getName(){
        return name;
    }
    void setLocation(String location){
        this.location = location;

    }
    String getLocation(){
        return location;
    }
    void setRating(int rating){
        this.rating = rating;
    }
    int getRating(){
        return rating;
    }
}
