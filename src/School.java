public class School {
    String name;
    String location;
    int number;

    void setName( String name){
        this.name = name;
    }
    String getName(){
        return name;
    }
    void setLocation(String location){
        this.location = location;

    }
    String getLocation(){
        return location;
    }
    void setNumber(int number){
        this.number = number;

    }
    int getNumber(){
        return number;
    }

}
